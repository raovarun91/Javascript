//Arithmetic Operations

let a = 10
let b = 5

console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a * b = ", a * b)
console.log("a / b = ", a / b)
console.log("a % b = ", a % b)
console.log("a ** b = ", a ** b)

//Unary Operator (Increment and decrement)
console.log("a =", a)
console.log("a++", a++)     //increment value after the opration is performed
console.log("a = ", a)      //Modified value after above increment which is above print + 1
console.log("++a", ++a)     //increment value before the operation is performed
console.log("a =", a)       //Modied value after above increment which is same as above printed value

console.log("a =", a)
console.log("a++", a--)     //decrement value after the opration is performed
console.log("a = ", a)      //Modified value after above decrement which is above print + 1
console.log("++a", --a)     //decrement value before the operation is performed
console.log("a =", a)       //Modied value after above decrement which is same as above printed value

